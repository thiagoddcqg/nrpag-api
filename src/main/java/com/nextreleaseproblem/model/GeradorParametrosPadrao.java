package com.nextreleaseproblem.model;


/**
 * Os valores padrão para os parâmetros do gerador
 */
public class GeradorParametrosPadrao {

	/**
	 * O número padrão de recursos a serem gerados
	 */
	public final static int NUMERO_DE_FEATURES = 20;

	/**
	 * O número padrão de funcionários a serem gerados
	 */
	public final static int NUMERO_DE_EMPREGADOS = 4;

	/**
	 * O número padrão de habilidades a serem geradas
	 */
	public final static int NUMERO_DE_HABILIDADES = 2;

	/**
	 * A taxa de precedência padrão para a geração
	 */
	public final static double TAXA_DE_PRECEDENCIA = 0.3;

	/**
	 * A duração máxima de um recurso
	 */
	public final static double MAXIMA_DURACAO_FEATURES = 40.0;

	/**
	 * A disponibilidade máxima semanal do funcionário
	 */
	public final static double MAXIMO_EMPREGADO_DISPONIBILIDADE_SEMANA = 35.0;

	/**
	 * A disponibilidade mínima semanal do funcionário
	 */
	public final static double MINIMO_EMPREGADO_DISPONIBILIDADE_SEMANA = 5.0;
}
